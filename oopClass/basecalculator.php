<?php
    class calculator
    {

        function add($first_no,$second_no)
        {
            return $first_no+$second_no;
        }
        function subtract($first_no,$second_no)
        {
            return $first_no-$second_no;
        }
        function multiply($first_no,$second_no)
        {
            return $first_no*$second_no;
        }
        function division($first_no,$second_no)
        {
            return $first_no/$second_no;
        }

    }