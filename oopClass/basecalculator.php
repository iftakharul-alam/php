<?php
    class basecalculator
    {

        function add_number($first_no,$second_no)
        {
            return $first_no+$second_no;
        }
        function sub_number($first_no,$second_no)
        {
            return $first_no-$second_no;
        }
        function mult_number($first_no,$second_no)
        {
            return $first_no*$second_no;
        }
        function div_number($first_no,$second_no)
        {
            return $first_no/$second_no;
        }

    }